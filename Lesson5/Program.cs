void WritePict(int[,] pict)
{
    for (int row = 0; row < pict.GetLength(0); row++)
    {
        for (int col = 0; col < pict.GetLength(1); col++)
        {
            if (pict[row, col] == 0) Console.Write(" ");
            else Console.Write("Ж");
        }
        Console.WriteLine();

    }

}

void FillArray(int[,] pict, int firstPointX, int firstPointY)
{
    if (firstPointX<0 || firstPointY<0 || firstPointX>=pict.GetLength(0) || firstPointY>=pict.GetLength(1))  return;
    if (pict[firstPointX, firstPointY] == 0)
    {
        pict[firstPointX, firstPointY] = 1;
        FillArray(pict, firstPointX,     firstPointY-1);
        FillArray(pict, firstPointX-1,   firstPointY);
        FillArray(pict, firstPointX,     firstPointY+1);
        FillArray(pict, firstPointX+1,   firstPointY);
    }
}

